using System;

namespace MyLearn.Module01_CSharpBasics
{
    /// <summary>
    /// –£—Ä–æ–∫ 4: –¶–∏–∫–ª—ã
    /// 
    /// –í —ç—Ç–æ–º —É—Ä–æ–∫–µ –≤—ã –∏–∑—É—á–∏—Ç–µ:
    /// - –¶–∏–∫–ª for
    /// - –¶–∏–∫–ª while
    /// - –¶–∏–∫–ª do-while
    /// - –¶–∏–∫–ª foreach
    /// - –û–ø–µ—Ä–∞—Ç–æ—Ä—ã break –∏ continue
    /// - –í–ª–æ–∂–µ–Ω–Ω—ã–µ —Ü–∏–∫–ª—ã
    /// </summary>
    public class Loops
    {
        // –î–ª—è –∑–∞–ø—É—Å–∫–∞ —ç—Ç–æ–≥–æ —É—Ä–æ–∫–∞ —Ä–∞—Å–∫–æ–º–º–µ–Ω—Ç–∏—Ä—É–π—Ç–µ Main –∏ –∑–∞–∫–æ–º–º–µ–Ω—Ç–∏—Ä—É–π—Ç–µ Main –≤ –¥—Ä—É–≥–∏—Ö —Ñ–∞–π–ª–∞—Ö
        public static void RunDemo()
        {
            Console.WriteLine("=== –£—Ä–æ–∫ 4: –¶–∏–∫–ª—ã ===\n");

            // ========================================
            // 1. –¶–ò–ö–õ FOR
            // ========================================

            Console.WriteLine("--- –¶–∏–∫–ª for ---");

            // –ë–∞–∑–æ–≤—ã–π —Å–∏–Ω—Ç–∞–∫—Å–∏—Å: for (–∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è; —É—Å–ª–æ–≤–∏–µ; –∏–∑–º–µ–Ω–µ–Ω–∏–µ)
            for (int i = 0; i < 5; i++)
            {
                Console.WriteLine($"–ò—Ç–µ—Ä–∞—Ü–∏—è {i}");
            }

            // –û–±—Ä–∞—Ç–Ω—ã–π –æ—Ç—Å—á–µ—Ç
            Console.WriteLine("\n–û–±—Ä–∞—Ç–Ω—ã–π –æ—Ç—Å—á–µ—Ç:");
            for (int i = 5; i > 0; i--)
            {
                Console.WriteLine($"{i}...");
            }
            Console.WriteLine("–ü—É—Å–∫! üöÄ\n");

            // –®–∞–≥ –±–æ–ª—å—à–µ 1
            Console.WriteLine("–ß–µ—Ç–Ω—ã–µ —á–∏—Å–ª–∞ –æ—Ç 0 –¥–æ 10:");
            for (int i = 0; i <= 10; i += 2)
            {
                Console.WriteLine(i);
            }

            Console.WriteLine();

            // ========================================
            // 2. –¶–ò–ö–õ WHILE
            // ========================================

            Console.WriteLine("--- –¶–∏–∫–ª while ---");

            // While –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è, –ø–æ–∫–∞ —É—Å–ª–æ–≤–∏–µ true
            int health = 100;
            int damage = 15;
            int turn = 1;

            Console.WriteLine($"–ù–∞—á–∞–ª—å–Ω–æ–µ –∑–¥–æ—Ä–æ–≤—å–µ: {health}");

            while (health > 0)
            {
                Console.WriteLine($"–•–æ–¥ {turn}: –ü–æ–ª—É—á–µ–Ω —É—Ä–æ–Ω {damage}");
                health -= damage;
                Console.WriteLine($"–ó–¥–æ—Ä–æ–≤—å–µ: {health}");
                turn++;

                if (health <= 0)
                {
                    Console.WriteLine("üíÄ –ü–µ—Ä—Å–æ–Ω–∞–∂ –ø–æ–≥–∏–±!");
                }
            }

            Console.WriteLine();

            // ========================================
            // 3. –¶–ò–ö–õ DO-WHILE
            // ========================================

            Console.WriteLine("--- –¶–∏–∫–ª do-while ---");

            // Do-while –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è –º–∏–Ω–∏–º—É–º 1 —Ä–∞–∑
            int attempts = 0;
            bool doorOpened = false;

            do
            {
                attempts++;
                Console.WriteLine($"–ü–æ–ø—ã—Ç–∫–∞ {attempts} –æ—Ç–∫—Ä—ã—Ç—å –¥–≤–µ—Ä—å...");

                // –°–∏–º—É–ª—è—Ü–∏—è —Å–ª—É—á–∞–π–Ω–æ–≥–æ –æ—Ç–∫—Ä—ã—Ç–∏—è (30% —à–∞–Ω—Å)
                Random random = new Random();
                doorOpened = random.Next(0, 100) < 30;

                if (doorOpened)
                {
                    Console.WriteLine("‚úÖ –î–≤–µ—Ä—å –æ—Ç–∫—Ä—ã—Ç–∞!");
                }
                else
                {
                    Console.WriteLine("‚ùå –î–≤–µ—Ä—å –≤—Å–µ –µ—â–µ –∑–∞–ø–µ—Ä—Ç–∞.");
                }

            } while (!doorOpened && attempts < 5);

            if (!doorOpened)
            {
                Console.WriteLine("üîí –ù–µ —É–¥–∞–ª–æ—Å—å –æ—Ç–∫—Ä—ã—Ç—å –¥–≤–µ—Ä—å –∑–∞ 5 –ø–æ–ø—ã—Ç–æ–∫.");
            }

            Console.WriteLine();

            // ========================================
            // 4. –¶–ò–ö–õ FOREACH
            // ========================================

            Console.WriteLine("--- –¶–∏–∫–ª foreach ---");

            // Foreach –¥–ª—è –ø–µ—Ä–µ–±–æ—Ä–∞ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –º–∞—Å—Å–∏–≤–∞
            string[] weapons = { "–ú–µ—á", "–õ—É–∫", "–ü–æ—Å–æ—Ö", "–¢–æ–ø–æ—Ä" };

            Console.WriteLine("–î–æ—Å—Ç—É–ø–Ω–æ–µ –æ—Ä—É–∂–∏–µ:");
            foreach (string weapon in weapons)
            {
                Console.WriteLine($"- {weapon}");
            }

            // Foreach —Å —á–∏—Å–ª–∞–º–∏
            int[] scores = { 100, 250, 175, 300, 425 };
            int totalScore = 0;

            Console.WriteLine("\n–û—á–∫–∏ –∑–∞ —É—Ä–æ–≤–Ω–∏:");
            foreach (int score in scores)
            {
                Console.WriteLine($"–£—Ä–æ–≤–µ–Ω—å: {score} –æ—á–∫–æ–≤");
                totalScore += score;
            }
            Console.WriteLine($"–í—Å–µ–≥–æ –æ—á–∫–æ–≤: {totalScore}");

            Console.WriteLine();

            // ========================================
            // 5. –û–ü–ï–†–ê–¢–û–†–´ BREAK –ò CONTINUE
            // ========================================

            Console.WriteLine("--- Break –∏ Continue ---");

            // Break - –≤—ã—Ö–æ–¥ –∏–∑ —Ü–∏–∫–ª–∞
            Console.WriteLine("–ü–æ–∏—Å–∫ –≤—Ä–∞–≥–∞:");
            for (int i = 1; i <= 10; i++)
            {
                Console.WriteLine($"–ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–æ–º–Ω–∞—Ç—É {i}...");

                if (i == 5)
                {
                    Console.WriteLine("üëπ –í—Ä–∞–≥ –Ω–∞–π–¥–µ–Ω –≤ –∫–æ–º–Ω–∞—Ç–µ 5!");
                    break; // –í—ã—Ö–æ–¥–∏–º –∏–∑ —Ü–∏–∫–ª–∞
                }
            }

            // Continue - –ø—Ä–æ–ø—É—Å–∫ –∏—Ç–µ—Ä–∞—Ü–∏–∏
            Console.WriteLine("\n–°–±–æ—Ä –ø—Ä–µ–¥–º–µ—Ç–æ–≤:");
            for (int i = 1; i <= 10; i++)
            {
                if (i % 2 == 0)
                {
                    continue; // –ü—Ä–æ–ø—É—Å–∫–∞–µ–º —á–µ—Ç–Ω—ã–µ —á–∏—Å–ª–∞
                }
                Console.WriteLine($"–°–æ–±—Ä–∞–Ω –ø—Ä–µ–¥–º–µ—Ç {i}");
            }

            Console.WriteLine();

            // ========================================
            // 6. –í–õ–û–ñ–ï–ù–ù–´–ï –¶–ò–ö–õ–´
            // ========================================

            Console.WriteLine("--- –í–ª–æ–∂–µ–Ω–Ω—ã–µ —Ü–∏–∫–ª—ã ---");

            // –°–æ–∑–¥–∞–Ω–∏–µ —Å–µ—Ç–∫–∏ —É—Ä–æ–≤–Ω—è
            Console.WriteLine("–ö–∞—Ä—Ç–∞ —É—Ä–æ–≤–Ω—è (5x5):");
            for (int row = 0; row < 5; row++)
            {
                for (int col = 0; col < 5; col++)
                {
                    Console.Write("[ ] ");
                }
                Console.WriteLine();
            }

            // –¢–∞–±–ª–∏—Ü–∞ —É–º–Ω–æ–∂–µ–Ω–∏—è
            Console.WriteLine("\n–¢–∞–±–ª–∏—Ü–∞ —É—Ä–æ–Ω–∞ (–æ—Ä—É–∂–∏–µ x –º–Ω–æ–∂–∏—Ç–µ–ª—å):");
            int[] weaponDamage = { 10, 15, 20 };
            float[] multipliers = { 1.0f, 1.5f, 2.0f };

            foreach (int baseDamage in weaponDamage)
            {
                Console.Write($"–£—Ä–æ–Ω {baseDamage}: ");
                foreach (float mult in multipliers)
                {
                    Console.Write($"{baseDamage * mult:F1} ");
                }
                Console.WriteLine();
            }

            Console.WriteLine();

            // ========================================
            // 7. –ò–ì–†–û–í–û–ô –ü–†–ò–ú–ï–†: –°–ò–°–¢–ï–ú–ê –°–ü–ê–í–ù–ê –í–†–ê–ì–û–í
            // ========================================

            Console.WriteLine("=== –ò–≥—Ä–æ–≤–æ–π –ø—Ä–∏–º–µ—Ä: –°–ø–∞–≤–Ω –≤—Ä–∞–≥–æ–≤ ===");

            int waveNumber = 1;
            int maxWaves = 3;

            for (int wave = 1; wave <= maxWaves; wave++)
            {
                Console.WriteLine($"\nüåä –í–û–õ–ù–ê {wave}");
                int enemiesInWave = wave * 3; // –ö–∞–∂–¥–∞—è –≤–æ–ª–Ω–∞ —Å–ª–æ–∂–Ω–µ–µ

                for (int enemy = 1; enemy <= enemiesInWave; enemy++)
                {
                    Console.WriteLine($"  –í—Ä–∞–≥ #{enemy} –ø–æ—è–≤–∏–ª—Å—è!");
                }

                Console.WriteLine($"–í—Å–µ–≥–æ –≤—Ä–∞–≥–æ–≤ –≤ –≤–æ–ª–Ω–µ: {enemiesInWave}");
            }

            // ========================================
            // 8. –ò–ì–†–û–í–û–ô –ü–†–ò–ú–ï–†: –°–ò–°–¢–ï–ú–ê –ò–ù–í–ï–ù–¢–ê–†–Ø
            // ========================================

            Console.WriteLine("\n=== –ò–≥—Ä–æ–≤–æ–π –ø—Ä–∏–º–µ—Ä: –ò–Ω–≤–µ–Ω—Ç–∞—Ä—å ===");

            string[] inventory = { "–ó–µ–ª—å–µ", "–ú–µ—á", "–©–∏—Ç", "–ö–ª—é—á", "–ó–µ–ª—å–µ" };

            Console.WriteLine("–°–æ–¥–µ—Ä–∂–∏–º–æ–µ –∏–Ω–≤–µ–Ω—Ç–∞—Ä—è:");
            int slotNumber = 1;
            foreach (string item in inventory)
            {
                Console.WriteLine($"–°–ª–æ—Ç {slotNumber}: {item}");
                slotNumber++;
            }

            // –ü–æ–∏—Å–∫ –ø—Ä–µ–¥–º–µ—Ç–∞
            string searchItem = "–ö–ª—é—á";
            bool found = false;
            int position = 0;

            Console.WriteLine($"\n–ü–æ–∏—Å–∫ –ø—Ä–µ–¥–º–µ—Ç–∞: {searchItem}");
            for (int i = 0; i < inventory.Length; i++)
            {
                if (inventory[i] == searchItem)
                {
                    found = true;
                    position = i + 1;
                    break;
                }
            }

            if (found)
            {
                Console.WriteLine($"‚úÖ {searchItem} –Ω–∞–π–¥–µ–Ω –≤ —Å–ª–æ—Ç–µ {position}");
            }
            else
            {
                Console.WriteLine($"‚ùå {searchItem} –Ω–µ –Ω–∞–π–¥–µ–Ω");
            }

            // –ü–æ–¥—Å—á–µ—Ç –∑–µ–ª–∏–π
            int potionCount = 0;
            foreach (string item in inventory)
            {
                if (item == "–ó–µ–ª—å–µ")
                {
                    potionCount++;
                }
            }
            Console.WriteLine($"–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –∑–µ–ª–∏–π: {potionCount}");

            // ========================================
            // 9. –ò–ì–†–û–í–û–ô –ü–†–ò–ú–ï–†: –ë–û–ï–í–ê–Ø –°–ò–°–¢–ï–ú–ê
            // ========================================

            Console.WriteLine("\n=== –ò–≥—Ä–æ–≤–æ–π –ø—Ä–∏–º–µ—Ä: –ë–æ–π ===");

            int playerHP = 100;
            int enemyHP = 80;
            int roundNumber = 1;

            Console.WriteLine("‚öîÔ∏è –ë–û–ô –ù–ê–ß–ê–õ–°–Ø!");
            Console.WriteLine($"–ò–≥—Ä–æ–∫: {playerHP} HP");
            Console.WriteLine($"–í—Ä–∞–≥: {enemyHP} HP\n");

            while (playerHP > 0 && enemyHP > 0)
            {
                Console.WriteLine($"--- –†–∞—É–Ω–¥ {roundNumber} ---");

                // –•–æ–¥ –∏–≥—Ä–æ–∫–∞
                int playerDamage = 20;
                enemyHP -= playerDamage;
                Console.WriteLine($"–ò–≥—Ä–æ–∫ –∞—Ç–∞–∫—É–µ—Ç! –£—Ä–æ–Ω: {playerDamage}");
                Console.WriteLine($"HP –≤—Ä–∞–≥–∞: {enemyHP}");

                if (enemyHP <= 0)
                {
                    Console.WriteLine("\nüéâ –ü–û–ë–ï–î–ê! –í—Ä–∞–≥ –ø–æ–≤–µ—Ä–∂–µ–Ω!");
                    break;
                }

                // –•–æ–¥ –≤—Ä–∞–≥–∞
                int enemyDamage = 15;
                playerHP -= enemyDamage;
                Console.WriteLine($"–í—Ä–∞–≥ –∞—Ç–∞–∫—É–µ—Ç! –£—Ä–æ–Ω: {enemyDamage}");
                Console.WriteLine($"HP –∏–≥—Ä–æ–∫–∞: {playerHP}");

                if (playerHP <= 0)
                {
                    Console.WriteLine("\nüíÄ –ü–û–†–ê–ñ–ï–ù–ò–ï! –ò–≥—Ä–æ–∫ –ø–æ–≥–∏–±...");
                    break;
                }

                roundNumber++;
                Console.WriteLine();
            }

            // ========================================
            // 10. –ò–ì–†–û–í–û–ô –ü–†–ò–ú–ï–†: –ì–ï–ù–ï–†–ê–¶–ò–Ø –£–†–û–í–ù–Ø
            // ========================================

            Console.WriteLine("\n=== –ò–≥—Ä–æ–≤–æ–π –ø—Ä–∏–º–µ—Ä: –ì–µ–Ω–µ—Ä–∞—Ü–∏—è —É—Ä–æ–≤–Ω—è ===");

            int levelWidth = 10;
            int levelHeight = 5;
            Random rnd = new Random();

            Console.WriteLine("–ü—Ä–æ—Ü–µ–¥—É—Ä–Ω–∞—è –≥–µ–Ω–µ—Ä–∞—Ü–∏—è —É—Ä–æ–≤–Ω—è:");
            for (int y = 0; y < levelHeight; y++)
            {
                for (int x = 0; x < levelWidth; x++)
                {
                    int tileType = rnd.Next(0, 100);

                    if (tileType < 70)
                    {
                        Console.Write("‚ñì "); // –ó–µ–º–ª—è (70%)
                    }
                    else if (tileType < 85)
                    {
                        Console.Write("‚ô¶ "); // –°–æ–∫—Ä–æ–≤–∏—â–µ (15%)
                    }
                    else if (tileType < 95)
                    {
                        Console.Write("‚ò† "); // –í—Ä–∞–≥ (10%)
                    }
                    else
                    {
                        Console.Write("‚òÖ "); // –ë–æ–Ω—É—Å (5%)
                    }
                }
                Console.WriteLine();
            }
        }
    }
}

