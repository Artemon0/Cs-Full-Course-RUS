using System;
using System.Collections.Generic;

namespace MyLearn.Module01_CSharpBasics
{
    /// <summary>
    /// –£—Ä–æ–∫ 5: –ú–∞—Å—Å–∏–≤—ã –∏ –∫–æ–ª–ª–µ–∫—Ü–∏–∏
    /// 
    /// –í —ç—Ç–æ–º —É—Ä–æ–∫–µ –≤—ã –∏–∑—É—á–∏—Ç–µ:
    /// - –ú–∞—Å—Å–∏–≤—ã (arrays)
    /// - –°–ø–∏—Å–∫–∏ (List<T>)
    /// - –°–ª–æ–≤–∞—Ä–∏ (Dictionary<TKey, TValue>)
    /// - –û—á–µ—Ä–µ–¥–∏ –∏ —Å—Ç–µ–∫–∏ (Queue, Stack)
    /// - –†–∞–±–æ—Ç–∞ —Å –∫–æ–ª–ª–µ–∫—Ü–∏—è–º–∏
    /// </summary>
    public class Collections
    {
        // –î–ª—è –∑–∞–ø—É—Å–∫–∞ —ç—Ç–æ–≥–æ —É—Ä–æ–∫–∞ —Ä–∞—Å–∫–æ–º–º–µ–Ω—Ç–∏—Ä—É–π—Ç–µ Main –∏ –∑–∞–∫–æ–º–º–µ–Ω—Ç–∏—Ä—É–π—Ç–µ Main –≤ –¥—Ä—É–≥–∏—Ö —Ñ–∞–π–ª–∞—Ö
        public static void RunDemo()
        {
            Console.WriteLine("=== –£—Ä–æ–∫ 5: –ú–∞—Å—Å–∏–≤—ã –∏ –∫–æ–ª–ª–µ–∫—Ü–∏–∏ ===\n");

            // ========================================
            // 1. –ú–ê–°–°–ò–í–´ (ARRAYS)
            // ========================================

            Console.WriteLine("--- –ú–∞—Å—Å–∏–≤—ã ---");

            // –û–±—ä—è–≤–ª–µ–Ω–∏–µ –∏ –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –º–∞—Å—Å–∏–≤–∞
            int[] scores = new int[5];  // –ú–∞—Å—Å–∏–≤ –∏–∑ 5 —ç–ª–µ–º–µ–Ω—Ç–æ–≤
            scores[0] = 100;
            scores[1] = 250;
            scores[2] = 175;
            scores[3] = 300;
            scores[4] = 425;

            Console.WriteLine("–û—á–∫–∏ –∑–∞ —É—Ä–æ–≤–Ω–∏:");
            for (int i = 0; i < scores.Length; i++)
            {
                Console.WriteLine($"–£—Ä–æ–≤–µ–Ω—å {i + 1}: {scores[i]} –æ—á–∫–æ–≤");
            }

            // –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –ø—Ä–∏ –æ–±—ä—è–≤–ª–µ–Ω–∏–∏
            string[] weapons = { "–ú–µ—á", "–õ—É–∫", "–ü–æ—Å–æ—Ö", "–¢–æ–ø–æ—Ä", "–ö–∏–Ω–∂–∞–ª" };
            Console.WriteLine($"\n–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –æ—Ä—É–∂–∏—è: {weapons.Length}");

            // –ú–Ω–æ–≥–æ–º–µ—Ä–Ω—ã–µ –º–∞—Å—Å–∏–≤—ã (–∫–∞—Ä—Ç–∞ —É—Ä–æ–≤–Ω—è)
            int[,] levelMap = new int[3, 3]
            {
                { 0, 1, 0 },  // 0 = –ø—É—Å—Ç–æ, 1 = —Å—Ç–µ–Ω–∞, 2 = –≤—Ä–∞–≥
                { 2, 0, 1 },
                { 0, 0, 2 }
            };

            Console.WriteLine("\n–ö–∞—Ä—Ç–∞ —É—Ä–æ–≤–Ω—è:");
            for (int row = 0; row < 3; row++)
            {
                for (int col = 0; col < 3; col++)
                {
                    char symbol = levelMap[row, col] switch
                    {
                        0 => '.',
                        1 => '#',
                        2 => 'E',
                        _ => '?'
                    };
                    Console.Write($"{symbol} ");
                }
                Console.WriteLine();
            }

            Console.WriteLine();

            // ========================================
            // 2. –°–ü–ò–°–ö–ò (LIST<T>)
            // ========================================

            Console.WriteLine("--- –°–ø–∏—Å–∫–∏ (List) ---");

            // List - –¥–∏–Ω–∞–º–∏—á–µ—Å–∫–∏–π –º–∞—Å—Å–∏–≤, –º–æ–∂–µ—Ç —Ä–∞—Å—Ç–∏
            List<string> inventory = new List<string>();

            // –î–æ–±–∞–≤–ª–µ–Ω–∏–µ —ç–ª–µ–º–µ–Ω—Ç–æ–≤
            inventory.Add("–ó–µ–ª—å–µ –∑–¥–æ—Ä–æ–≤—å—è");
            inventory.Add("–ó–µ–ª—å–µ –º–∞–Ω—ã");
            inventory.Add("–ö–ª—é—á");
            inventory.Add("–ú–µ—á");

            Console.WriteLine($"–ü—Ä–µ–¥–º–µ—Ç–æ–≤ –≤ –∏–Ω–≤–µ–Ω—Ç–∞—Ä–µ: {inventory.Count}");

            // –ü–µ—Ä–µ–±–æ—Ä —ç–ª–µ–º–µ–Ω—Ç–æ–≤
            Console.WriteLine("–°–æ–¥–µ—Ä–∂–∏–º–æ–µ –∏–Ω–≤–µ–Ω—Ç–∞—Ä—è:");
            foreach (string item in inventory)
            {
                Console.WriteLine($"- {item}");
            }

            // –î–æ—Å—Ç—É–ø –ø–æ –∏–Ω–¥–µ–∫—Å—É
            Console.WriteLine($"\n–ü–µ—Ä–≤—ã–π –ø—Ä–µ–¥–º–µ—Ç: {inventory[0]}");
            Console.WriteLine($"–ü–æ—Å–ª–µ–¥–Ω–∏–π –ø—Ä–µ–¥–º–µ—Ç: {inventory[inventory.Count - 1]}");

            // –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è
            bool hasKey = inventory.Contains("–ö–ª—é—á");
            Console.WriteLine($"–ï—Å—Ç—å –∫–ª—é—á: {hasKey}");

            // –£–¥–∞–ª–µ–Ω–∏–µ —ç–ª–µ–º–µ–Ω—Ç–∞
            inventory.Remove("–ó–µ–ª—å–µ –º–∞–Ω—ã");
            Console.WriteLine($"–ü–æ—Å–ª–µ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è –∑–µ–ª—å—è: {inventory.Count} –ø—Ä–µ–¥–º–µ—Ç–æ–≤");

            // –í—Å—Ç–∞–≤–∫–∞ —ç–ª–µ–º–µ–Ω—Ç–∞
            inventory.Insert(0, "–ó–æ–ª–æ—Ç–æ");
            Console.WriteLine($"–ü–µ—Ä–≤—ã–π –ø—Ä–µ–¥–º–µ—Ç —Ç–µ–ø–µ—Ä—å: {inventory[0]}");

            // –û—á–∏—Å—Ç–∫–∞ —Å–ø–∏—Å–∫–∞
            // inventory.Clear();

            Console.WriteLine();

            // ========================================
            // 3. –°–õ–û–í–ê–†–ò (DICTIONARY<TKEY, TVALUE>)
            // ========================================

            Console.WriteLine("--- –°–ª–æ–≤–∞—Ä–∏ (Dictionary) ---");

            // Dictionary - –ø–∞—Ä—ã –∫–ª—é—á-–∑–Ω–∞—á–µ–Ω–∏–µ
            Dictionary<string, int> playerStats = new Dictionary<string, int>();

            // –î–æ–±–∞–≤–ª–µ–Ω–∏–µ —ç–ª–µ–º–µ–Ω—Ç–æ–≤
            playerStats.Add("–ó–¥–æ—Ä–æ–≤—å–µ", 100);
            playerStats.Add("–ú–∞–Ω–∞", 50);
            playerStats.Add("–°–∏–ª–∞", 15);
            playerStats.Add("–õ–æ–≤–∫–æ—Å—Ç—å", 12);
            playerStats.Add("–ò–Ω—Ç–µ–ª–ª–µ–∫—Ç", 10);

            Console.WriteLine("–•–∞—Ä–∞–∫—Ç–µ—Ä–∏—Å—Ç–∏–∫–∏ –∏–≥—Ä–æ–∫–∞:");
            foreach (KeyValuePair<string, int> stat in playerStats)
            {
                Console.WriteLine($"{stat.Key}: {stat.Value}");
            }

            // –î–æ—Å—Ç—É–ø –ø–æ –∫–ª—é—á—É
            Console.WriteLine($"\n–¢–µ–∫—É—â–µ–µ –∑–¥–æ—Ä–æ–≤—å–µ: {playerStats["–ó–¥–æ—Ä–æ–≤—å–µ"]}");

            // –ò–∑–º–µ–Ω–µ–Ω–∏–µ –∑–Ω–∞—á–µ–Ω–∏—è
            playerStats["–ó–¥–æ—Ä–æ–≤—å–µ"] = 80;
            Console.WriteLine($"–ü–æ—Å–ª–µ —É—Ä–æ–Ω–∞: {playerStats["–ó–¥–æ—Ä–æ–≤—å–µ"]}");

            // –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è –∫–ª—é—á–∞
            if (playerStats.ContainsKey("–ú–∞–Ω–∞"))
            {
                Console.WriteLine($"–ú–∞–Ω–∞: {playerStats["–ú–∞–Ω–∞"]}");
            }

            // –ë–µ–∑–æ–ø–∞—Å–Ω–æ–µ –ø–æ–ª—É—á–µ–Ω–∏–µ –∑–Ω–∞—á–µ–Ω–∏—è
            if (playerStats.TryGetValue("–°–∏–ª–∞", out int strength))
            {
                Console.WriteLine($"–°–∏–ª–∞ –ø–µ—Ä—Å–æ–Ω–∞–∂–∞: {strength}");
            }

            // –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –ø—Ä–∏ –æ–±—ä—è–≤–ª–µ–Ω–∏–∏
            Dictionary<string, float> weaponDamage = new Dictionary<string, float>
            {
                { "–ú–µ—á", 50.0f },
                { "–õ—É–∫", 35.0f },
                { "–ü–æ—Å–æ—Ö", 45.0f },
                { "–¢–æ–ø–æ—Ä", 65.0f }
            };

            Console.WriteLine("\n–£—Ä–æ–Ω –æ—Ä—É–∂–∏—è:");
            foreach (var weapon in weaponDamage)
            {
                Console.WriteLine($"{weapon.Key}: {weapon.Value} —É—Ä–æ–Ω–∞");
            }

            Console.WriteLine();

            // ========================================
            // 4. –û–ß–ï–†–ï–î–ò (QUEUE<T>)
            // ========================================

            Console.WriteLine("--- –û—á–µ—Ä–µ–¥–∏ (Queue) ---");

            // Queue - –ø–µ—Ä–≤—ã–π –≤–æ—à–µ–ª, –ø–µ—Ä–≤—ã–π –≤—ã—à–µ–ª (FIFO)
            Queue<string> questQueue = new Queue<string>();

            // –î–æ–±–∞–≤–ª–µ–Ω–∏–µ –≤ –æ—á–µ—Ä–µ–¥—å
            questQueue.Enqueue("–£–±–∏—Ç—å 10 –≥–æ–±–ª–∏–Ω–æ–≤");
            questQueue.Enqueue("–°–æ–±—Ä–∞—Ç—å 5 —Ç—Ä–∞–≤");
            questQueue.Enqueue("–ù–∞–π—Ç–∏ –∞—Ä—Ç–µ—Ñ–∞–∫—Ç");

            Console.WriteLine($"–ö–≤–µ—Å—Ç–æ–≤ –≤ –æ—á–µ—Ä–µ–¥–∏: {questQueue.Count}");

            // –ü—Ä–æ—Å–º–æ—Ç—Ä –ø–µ—Ä–≤–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞
            string nextQuest = questQueue.Peek();
            Console.WriteLine($"–°–ª–µ–¥—É—é—â–∏–π –∫–≤–µ—Å—Ç: {nextQuest}");

            // –í—ã–ø–æ–ª–Ω–µ–Ω–∏–µ –∫–≤–µ—Å—Ç–æ–≤
            while (questQueue.Count > 0)
            {
                string quest = questQueue.Dequeue();
                Console.WriteLine($"‚úÖ –í—ã–ø–æ–ª–Ω–µ–Ω: {quest}");
            }

            Console.WriteLine();

            // ========================================
            // 5. –°–¢–ï–ö–ò (STACK<T>)
            // ========================================

            Console.WriteLine("--- –°—Ç–µ–∫–∏ (Stack) ---");

            // Stack - –ø–æ—Å–ª–µ–¥–Ω–∏–π –≤–æ—à–µ–ª, –ø–µ—Ä–≤—ã–π –≤—ã—à–µ–ª (LIFO)
            Stack<string> actionHistory = new Stack<string>();

            // –î–æ–±–∞–≤–ª–µ–Ω–∏–µ –¥–µ–π—Å—Ç–≤–∏–π
            actionHistory.Push("–û—Ç–∫—Ä—ã–ª –¥–≤–µ—Ä—å");
            actionHistory.Push("–í–∑—è–ª –∫–ª—é—á");
            actionHistory.Push("–ü–æ–±–µ–¥–∏–ª –≤—Ä–∞–≥–∞");
            actionHistory.Push("–ü–æ–¥–æ–±—Ä–∞–ª –º–µ—á");

            Console.WriteLine($"–î–µ–π—Å—Ç–≤–∏–π –≤ –∏—Å—Ç–æ—Ä–∏–∏: {actionHistory.Count}");

            // –ü—Ä–æ—Å–º–æ—Ç—Ä –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ –¥–µ–π—Å—Ç–≤–∏—è
            string lastAction = actionHistory.Peek();
            Console.WriteLine($"–ü–æ—Å–ª–µ–¥–Ω–µ–µ –¥–µ–π—Å—Ç–≤–∏–µ: {lastAction}");

            // –û—Ç–º–µ–Ω–∞ –¥–µ–π—Å—Ç–≤–∏–π (undo)
            Console.WriteLine("\n–û—Ç–º–µ–Ω–∞ –¥–µ–π—Å—Ç–≤–∏–π:");
            while (actionHistory.Count > 0)
            {
                string action = actionHistory.Pop();
                Console.WriteLine($"‚Ü∂ –û—Ç–º–µ–Ω–µ–Ω–æ: {action}");
            }

            Console.WriteLine();

            // ========================================
            // 6. –ò–ì–†–û–í–û–ô –ü–†–ò–ú–ï–†: –°–ò–°–¢–ï–ú–ê –ò–ù–í–ï–ù–¢–ê–†–Ø
            // ========================================

            Console.WriteLine("=== –ò–≥—Ä–æ–≤–æ–π –ø—Ä–∏–º–µ—Ä: –ü—Ä–æ–¥–≤–∏–Ω—É—Ç—ã–π –∏–Ω–≤–µ–Ω—Ç–∞—Ä—å ===");

            // –ò–Ω–≤–µ–Ω—Ç–∞—Ä—å —Å –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ–º –ø—Ä–µ–¥–º–µ—Ç–æ–≤
            Dictionary<string, int> advancedInventory = new Dictionary<string, int>
            {
                { "–ó–µ–ª—å–µ –∑–¥–æ—Ä–æ–≤—å—è", 5 },
                { "–ó–µ–ª—å–µ –º–∞–Ω—ã", 3 },
                { "–°—Ç—Ä–µ–ª—ã", 50 },
                { "–ó–æ–ª–æ—Ç–æ", 150 }
            };

            Console.WriteLine("–ò–Ω–≤–µ–Ω—Ç–∞—Ä—å:");
            foreach (var item in advancedInventory)
            {
                Console.WriteLine($"{item.Key}: x{item.Value}");
            }

            // –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ –ø—Ä–µ–¥–º–µ—Ç–∞
            string useItem = "–ó–µ–ª—å–µ –∑–¥–æ—Ä–æ–≤—å—è";
            if (advancedInventory.ContainsKey(useItem))
            {
                advancedInventory[useItem]--;
                Console.WriteLine($"\nüß™ –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–æ: {useItem}");

                if (advancedInventory[useItem] <= 0)
                {
                    advancedInventory.Remove(useItem);
                    Console.WriteLine($"‚ùå {useItem} –∑–∞–∫–æ–Ω—á–∏–ª–∏—Å—å!");
                }
                else
                {
                    Console.WriteLine($"–û—Å—Ç–∞–ª–æ—Å—å: {advancedInventory[useItem]}");
                }
            }

            // –î–æ–±–∞–≤–ª–µ–Ω–∏–µ –ø—Ä–µ–¥–º–µ—Ç–∞
            string foundItem = "–ó–µ–ª—å–µ –º–∞–Ω—ã";
            if (advancedInventory.ContainsKey(foundItem))
            {
                advancedInventory[foundItem] += 2;
            }
            else
            {
                advancedInventory.Add(foundItem, 2);
            }
            Console.WriteLine($"\n‚ú® –ù–∞–π–¥–µ–Ω–æ: {foundItem} x2");
            Console.WriteLine($"–í—Å–µ–≥–æ {foundItem}: {advancedInventory[foundItem]}");

            // ========================================
            // 7. –ò–ì–†–û–í–û–ô –ü–†–ò–ú–ï–†: –°–ò–°–¢–ï–ú–ê –í–†–ê–ì–û–í
            // ========================================

            Console.WriteLine("\n=== –ò–≥—Ä–æ–≤–æ–π –ø—Ä–∏–º–µ—Ä: –°–ø–∏—Å–æ–∫ –≤—Ä–∞–≥–æ–≤ ===");

            List<string> enemies = new List<string>
            {
                "–ì–æ–±–ª–∏–Ω",
                "–û—Ä–∫",
                "–ó–æ–º–±–∏",
                "–°–∫–µ–ª–µ—Ç",
                "–î—Ä–∞–∫–æ–Ω"
            };

            List<int> enemyHealth = new List<int>
            {
                30,
                50,
                40,
                25,
                200
            };

            Console.WriteLine("–í—Ä–∞–≥–∏ –Ω–∞ —É—Ä–æ–≤–Ω–µ:");
            for (int i = 0; i < enemies.Count; i++)
            {
                Console.WriteLine($"{i + 1}. {enemies[i]} - {enemyHealth[i]} HP");
            }

            // –ê—Ç–∞–∫–∞ –≤—Ä–∞–≥–∞
            int targetIndex = 2; // –ó–æ–º–±–∏
            int damage = 40;

            Console.WriteLine($"\n‚öîÔ∏è –ê—Ç–∞–∫—É–µ–º {enemies[targetIndex]}!");
            enemyHealth[targetIndex] -= damage;

            if (enemyHealth[targetIndex] <= 0)
            {
                Console.WriteLine($"üíÄ {enemies[targetIndex]} –ø–æ–≤–µ—Ä–∂–µ–Ω!");
                enemies.RemoveAt(targetIndex);
                enemyHealth.RemoveAt(targetIndex);
            }
            else
            {
                Console.WriteLine($"{enemies[targetIndex]} HP: {enemyHealth[targetIndex]}");
            }

            Console.WriteLine($"\n–û—Å—Ç–∞–ª–æ—Å—å –≤—Ä–∞–≥–æ–≤: {enemies.Count}");

            // ========================================
            // 8. –ò–ì–†–û–í–û–ô –ü–†–ò–ú–ï–†: –°–ò–°–¢–ï–ú–ê –î–û–°–¢–ò–ñ–ï–ù–ò–ô
            // ========================================

            Console.WriteLine("\n=== –ò–≥—Ä–æ–≤–æ–π –ø—Ä–∏–º–µ—Ä: –î–æ—Å—Ç–∏–∂–µ–Ω–∏—è ===");

            Dictionary<string, bool> achievements = new Dictionary<string, bool>
            {
                { "–ü–µ—Ä–≤–∞—è –∫—Ä–æ–≤—å", false },
                { "–ü–æ–±–µ–¥–∏—Ç–µ–ª—å", false },
                { "–ö–æ–ª–ª–µ–∫—Ü–∏–æ–Ω–µ—Ä", false },
                { "–ù–µ—É—è–∑–≤–∏–º—ã–π", false },
                { "–ú–∞—Å—Ç–µ—Ä", false }
            };

            Console.WriteLine("–ü—Ä–æ–≥—Ä–µ—Å—Å –¥–æ—Å—Ç–∏–∂–µ–Ω–∏–π:");
            foreach (var achievement in achievements)
            {
                string status = achievement.Value ? "‚úÖ" : "‚ùå";
                Console.WriteLine($"{status} {achievement.Key}");
            }

            // –†–∞–∑–±–ª–æ–∫–∏—Ä–æ–≤–∫–∞ –¥–æ—Å—Ç–∏–∂–µ–Ω–∏—è
            string unlockedAchievement = "–ü–µ—Ä–≤–∞—è –∫—Ä–æ–≤—å";
            if (achievements.ContainsKey(unlockedAchievement))
            {
                achievements[unlockedAchievement] = true;
                Console.WriteLine($"\nüèÜ –î–æ—Å—Ç–∏–∂–µ–Ω–∏–µ —Ä–∞–∑–±–ª–æ–∫–∏—Ä–æ–≤–∞–Ω–æ: {unlockedAchievement}!");
            }

            // –ü–æ–¥—Å—á–µ—Ç —Ä–∞–∑–±–ª–æ–∫–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö
            int unlockedCount = 0;
            foreach (var achievement in achievements)
            {
                if (achievement.Value)
                {
                    unlockedCount++;
                }
            }

            Console.WriteLine($"–†–∞–∑–±–ª–æ–∫–∏—Ä–æ–≤–∞–Ω–æ: {unlockedCount}/{achievements.Count}");
        }
    }
}

