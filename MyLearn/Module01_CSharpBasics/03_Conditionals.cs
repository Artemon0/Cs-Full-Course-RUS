using System;

namespace MyLearn.Module01_CSharpBasics
{
    /// <summary>
    /// Lesson 3: Conditional Statements
    /// 
    /// –í —ç—Ç–æ–º —É—Ä–æ–∫–µ –≤—ã –∏–∑—É—á–∏—Ç–µ:
    /// - if operator –∏ if-else
    /// - –ú–Ω–æ–∂–µ—Å—Ç–≤–µ–Ω–Ω—ã–µ conditions (else if)
    /// - –í–ª–æ–∂–µ–Ω–Ω—ã–µ conditions
    /// - switch operator
    /// - –¢–µ—Ä–Ω–∞—Ä–Ω—ã–π –æ–ø–µ—Ä–∞—Ç–æ—Ä
    /// </summary>
    public class Conditionals
    {
        // –î–ª—è –∑–∞–ø—É—Å–∫–∞ —ç—Ç–æ–≥–æ —É—Ä–æ–∫–∞ —Ä–∞—Å–∫–æ–º–º–µ–Ω—Ç–∏—Ä—É–π—Ç–µ Main –∏ –∑–∞–∫–æ–º–º–µ–Ω—Ç–∏—Ä—É–π—Ç–µ Main –≤ –¥—Ä—É–≥–∏—Ö —Ñ–∞–π–ª–∞—Ö
        public static void RunDemo()
        {
            Console.WriteLine("=== Lesson 3: Conditional Statements ===\n");

            // ========================================
            // 1. –û–ü–ï–†–ê–¢–û–† IF
            // ========================================

            Console.WriteLine("--- if operator ---");

            int playerHealth = 30;

            // –ü—Ä–æ—Å—Ç–æ–µ —É—Å–ª–æ–≤–∏–µ
            if (playerHealth < 50)
            {
                Console.WriteLine("‚ö†Ô∏è –ú–∞–ª–æ –∑–¥–æ—Ä–æ–≤—å—è! –ù—É–∂–Ω–æ –ª–µ—á–µ–Ω–∏–µ.");
            }

            // –£—Å–ª–æ–≤–∏–µ —Å –Ω–µ—Å–∫–æ–ª—å–∫–∏–º–∏ –¥–µ–π—Å—Ç–≤–∏—è–º–∏
            int enemyDistance = 5;
            if (enemyDistance <= 10)
            {
                Console.WriteLine("üéØ –í—Ä–∞–≥ –±–ª–∏–∑–∫–æ!");
                Console.WriteLine("‚öîÔ∏è –ê—Ç–∞–∫—É–µ–º!");
            }

            Console.WriteLine();

            // ========================================
            // 2. –û–ü–ï–†–ê–¢–û–† IF-ELSE
            // ========================================

            Console.WriteLine("--- if operator-else ---");

            bool hasKey = true;

            if (hasKey)
            {
                Console.WriteLine("üîì –î–≤–µ—Ä—å –æ—Ç–∫—Ä—ã—Ç–∞! –ü—Ä–æ—Ö–æ–¥–∏–º –¥–∞–ª—å—à–µ.");
            }
            else
            {
                Console.WriteLine("üîí –î–≤–µ—Ä—å –∑–∞–ø–µ—Ä—Ç–∞. –ù—É–∂–µ–Ω –∫–ª—é—á.");
            }

            // –ü—Ä–æ–≤–µ—Ä–∫–∞ –±–æ–µ–ø—Ä–∏–ø–∞—Å–æ–≤
            int ammo = 0;

            if (ammo > 0)
            {
                Console.WriteLine($"üí• –°—Ç—Ä–µ–ª—è–µ–º! –û—Å—Ç–∞–ª–æ—Å—å –ø–∞—Ç—Ä–æ–Ω–æ–≤: {ammo - 1}");
            }
            else
            {
                Console.WriteLine("üîÑ –ù–µ—Ç –ø–∞—Ç—Ä–æ–Ω–æ–≤! –ü–µ—Ä–µ–∑–∞—Ä—è–¥–∫–∞...");
            }

            Console.WriteLine();

            // ========================================
            // 3. –ú–ù–û–ñ–ï–°–¢–í–ï–ù–ù–´–ï –£–°–õ–û–í–ò–Ø (ELSE IF)
            // ========================================

            Console.WriteLine("--- –ú–Ω–æ–∂–µ—Å—Ç–≤–µ–Ω–Ω—ã–µ conditions ---");

            int health = 75;

            if (health >= 80)
            {
                Console.WriteLine("üíö –ó–¥–æ—Ä–æ–≤—å–µ –æ—Ç–ª–∏—á–Ω–æ–µ!");
            }
            else if (health >= 50)
            {
                Console.WriteLine("üíõ –ó–¥–æ—Ä–æ–≤—å–µ –Ω–æ—Ä–º–∞–ª—å–Ω–æ–µ.");
            }
            else if (health >= 20)
            {
                Console.WriteLine("üß° –ú–∞–ª–æ –∑–¥–æ—Ä–æ–≤—å—è!");
            }
            else
            {
                Console.WriteLine("‚ù§Ô∏è –ö—Ä–∏—Ç–∏—á–µ—Å–∫–æ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ!");
            }

            // –°–∏—Å—Ç–µ–º–∞ —Ä–∞–Ω–≥–æ–≤
            int score = 8500;
            string rank;

            if (score >= 10000)
            {
                rank = "S";
            }
            else if (score >= 7500)
            {
                rank = "A";
            }
            else if (score >= 5000)
            {
                rank = "B";
            }
            else if (score >= 2500)
            {
                rank = "C";
            }
            else
            {
                rank = "D";
            }

            Console.WriteLine($"–í–∞—à —Ä–∞–Ω–≥: {rank} (–û—á–∫–∏: {score})");
            Console.WriteLine();

            // ========================================
            // 4. –í–õ–û–ñ–ï–ù–ù–´–ï –£–°–õ–û–í–ò–Ø
            // ========================================

            Console.WriteLine("--- –í–ª–æ–∂–µ–Ω–Ω—ã–µ conditions ---");

            bool isAlive = true;
            int mana = 60;
            int manaCost = 50;

            if (isAlive)
            {
                if (mana >= manaCost)
                {
                    Console.WriteLine("‚ú® –ó–∞–∫–ª–∏–Ω–∞–Ω–∏–µ –ø—Ä–∏–º–µ–Ω–µ–Ω–æ!");
                    mana -= manaCost;
                    Console.WriteLine($"–û—Å—Ç–∞–ª–æ—Å—å –º–∞–Ω—ã: {mana}");
                }
                else
                {
                    Console.WriteLine("‚ùå –ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –º–∞–Ω—ã!");
                }
            }
            else
            {
                Console.WriteLine("üíÄ –ü–µ—Ä—Å–æ–Ω–∞–∂ –º–µ—Ä—Ç–≤. –ù–µ–≤–æ–∑–º–æ–∂–Ω–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –∑–∞–∫–ª–∏–Ω–∞–Ω–∏–µ.");
            }

            Console.WriteLine();

            // ========================================
            // 5. –õ–û–ì–ò–ß–ï–°–ö–ò–ï –û–ü–ï–†–ê–¢–û–†–´ –í –£–°–õ–û–í–ò–Ø–•
            // ========================================

            Console.WriteLine("--- –õ–æ–≥–∏—á–µ—Å–∫–∏–µ operators ---");

            int level = 10;
            bool hasWeapon = true;
            bool hasArmor = false;

            // –û–ø–µ—Ä–∞—Ç–æ—Ä –ò (&&)
            if (level >= 5 && hasWeapon)
            {
                Console.WriteLine("‚úÖ –ú–æ–∂–Ω–æ –∏–¥—Ç–∏ –≤ –ø–æ–¥–∑–µ–º–µ–ª—å–µ!");
            }

            // –û–ø–µ—Ä–∞—Ç–æ—Ä –ò–õ–ò (||)
            if (hasWeapon || hasArmor)
            {
                Console.WriteLine("‚öîÔ∏è –ï—Å—Ç—å —ç–∫–∏–ø–∏—Ä–æ–≤–∫–∞ –¥–ª—è –±–æ—è.");
            }

            // –û–ø–µ—Ä–∞—Ç–æ—Ä –ù–ï (!)
            if (!hasArmor)
            {
                Console.WriteLine("üõ°Ô∏è –†–µ–∫–æ–º–µ–Ω–¥—É–µ—Ç—Å—è –∫—É–ø–∏—Ç—å –±—Ä–æ–Ω—é.");
            }

            // –°–ª–æ–∂–Ω–æ–µ —É—Å–ª–æ–≤–∏–µ
            int gold = 150;
            int armorPrice = 100;

            if (gold >= armorPrice && !hasArmor && level >= 3)
            {
                Console.WriteLine("üí∞ –ú–æ–∂–Ω–æ –∫—É–ø–∏—Ç—å –±—Ä–æ–Ω—é!");
            }

            Console.WriteLine();

            // ========================================
            // 6. –û–ü–ï–†–ê–¢–û–† SWITCH
            // ========================================

            Console.WriteLine("--- switch operator ---");

            string weaponType = "Sword";

            switch (weaponType)
            {
                case "Sword":
                    Console.WriteLine("‚öîÔ∏è –ú–µ—á: –£—Ä–æ–Ω 50, –°–∫–æ—Ä–æ—Å—Ç—å —Å—Ä–µ–¥–Ω—è—è");
                    break;
                case "Bow":
                    Console.WriteLine("üèπ –õ—É–∫: –£—Ä–æ–Ω 30, –î–∞–ª—å–Ω—è—è –∞—Ç–∞–∫–∞");
                    break;
                case "Staff":
                    Console.WriteLine("ü™Ñ –ü–æ—Å–æ—Ö: –£—Ä–æ–Ω 40, –ú–∞–≥–∏—á–µ—Å–∫–∏–π —É—Ä–æ–Ω");
                    break;
                case "Axe":
                    Console.WriteLine("ü™ì –¢–æ–ø–æ—Ä: –£—Ä–æ–Ω 70, –°–∫–æ—Ä–æ—Å—Ç—å –Ω–∏–∑–∫–∞—è");
                    break;
                default:
                    Console.WriteLine("‚ùì –ù–µ–∏–∑–≤–µ—Å—Ç–Ω–æ–µ –æ—Ä—É–∂–∏–µ");
                    break;
            }

            // Switch —Å —á–∏—Å–ª–∞–º–∏
            int dayOfWeek = 3;

            switch (dayOfWeek)
            {
                case 1:
                    Console.WriteLine("–ü–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫ - –ö–≤–µ—Å—Ç –Ω–∞ –≥–æ–±–ª–∏–Ω–æ–≤");
                    break;
                case 2:
                    Console.WriteLine("–í—Ç–æ—Ä–Ω–∏–∫ - –ö–≤–µ—Å—Ç –Ω–∞ –æ—Ä–∫–æ–≤");
                    break;
                case 3:
                    Console.WriteLine("–°—Ä–µ–¥–∞ - –ö–≤–µ—Å—Ç –Ω–∞ –¥—Ä–∞–∫–æ–Ω–æ–≤");
                    break;
                case 4:
                case 5:
                    Console.WriteLine("–ß–µ—Ç–≤–µ—Ä–≥/–ü—è—Ç–Ω–∏—Ü–∞ - –†–µ–π–¥ –Ω–∞ –±–æ—Å—Å–∞");
                    break;
                default:
                    Console.WriteLine("–í—ã—Ö–æ–¥–Ω—ã–µ - –û—Ç–¥—ã—Ö");
                    break;
            }

            Console.WriteLine();

            // ========================================
            // 7. –¢–ï–†–ù–ê–†–ù–´–ô –û–ü–ï–†–ê–¢–û–†
            // ========================================

            Console.WriteLine("--- –¢–µ—Ä–Ω–∞—Ä–Ω—ã–π –æ–ø–µ—Ä–∞—Ç–æ—Ä ---");

            // –°–∏–Ω—Ç–∞–∫—Å–∏—Å: —É—Å–ª–æ–≤–∏–µ ? –∑–Ω–∞—á–µ–Ω–∏–µ_–µ—Å–ª–∏_true : –∑–Ω–∞—á–µ–Ω–∏–µ_–µ—Å–ª–∏_false

            int currentHealth = 45;
            string healthStatus = currentHealth > 50 ? "–ó–¥–æ—Ä–æ–≤" : "–†–∞–Ω–µ–Ω";
            Console.WriteLine($"Status: {healthStatus}");

            // –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —É—Ä–æ–Ω–∞
            bool isCritical = true;
            int baseDamage = 30;
            int damage = isCritical ? baseDamage * 2 : baseDamage;
            Console.WriteLine($"–£—Ä–æ–Ω: {damage} {(isCritical ? "(–ö—Ä–∏—Ç!)" : "")}");

            // –í–ª–æ–∂–µ–Ω–Ω—ã–π —Ç–µ—Ä–Ω–∞—Ä–Ω—ã–π –æ–ø–µ—Ä–∞—Ç–æ—Ä (–Ω–µ —Ä–µ–∫–æ–º–µ–Ω–¥—É–µ—Ç—Å—è, –Ω–æ –≤–æ–∑–º–æ–∂–Ω–æ)
            int playerLevel = 15;
            string difficulty = playerLevel >= 20 ? "–°–ª–æ–∂–Ω–æ" :
                               playerLevel >= 10 ? "–°—Ä–µ–¥–Ω–µ" : "–õ–µ–≥–∫–æ";
            Console.WriteLine($"–°–ª–æ–∂–Ω–æ—Å—Ç—å: {difficulty}");

            Console.WriteLine();

            // ========================================
            // 8. –ò–ì–†–û–í–û–ô –ü–†–ò–ú–ï–†: –°–ò–°–¢–ï–ú–ê –î–ò–ê–õ–û–ì–û–í
            // ========================================

            Console.WriteLine("=== –ò–≥—Ä–æ–≤–æ–π –ø—Ä–∏–º–µ—Ä: –°–∏—Å—Ç–µ–º–∞ –¥–∏–∞–ª–æ–≥–æ–≤ ===");

            string playerClass = "Warrior";
            int reputation = 75;
            bool questCompleted = true;

            Console.WriteLine($"–ö–ª–∞—Å—Å: {playerClass}");
            Console.WriteLine($"–†–µ–ø—É—Ç–∞—Ü–∏—è: {reputation}");
            Console.WriteLine($"–ö–≤–µ—Å—Ç –≤—ã–ø–æ–ª–Ω–µ–Ω: {questCompleted}\n");

            // NPC —Ä–µ–∞–≥–∏—Ä—É–µ—Ç –Ω–∞ –∏–≥—Ä–æ–∫–∞
            if (reputation >= 80)
            {
                Console.WriteLine("NPC: –ü—Ä–∏–≤–µ—Ç—Å—Ç–≤—É—é, –¥—Ä—É–≥! –†–∞–¥ —Ç–µ–±—è –≤–∏–¥–µ—Ç—å!");
            }
            else if (reputation >= 50)
            {
                Console.WriteLine("NPC: –ó–¥—Ä–∞–≤—Å—Ç–≤—É–π, –ø—É—Ç–Ω–∏–∫.");
            }
            else if (reputation >= 20)
            {
                Console.WriteLine("NPC: –ß—Ç–æ —Ç–µ–±–µ –Ω—É–∂–Ω–æ?");
            }
            else
            {
                Console.WriteLine("NPC: –£–±–∏—Ä–∞–π—Å—è –æ—Ç—Å—é–¥–∞!");
            }

            // –î–æ—Å—Ç—É–ø –∫ –∫–≤–µ—Å—Ç—É
            if (questCompleted && reputation >= 50)
            {
                Console.WriteLine("NPC: –£ –º–µ–Ω—è –µ—Å—Ç—å –¥–ª—è —Ç–µ–±—è –æ—Å–æ–±–æ–µ –∑–∞–¥–∞–Ω–∏–µ!");

                switch (playerClass)
                {
                    case "Warrior":
                        Console.WriteLine("–ö–≤–µ—Å—Ç: –ü–æ–±–µ–¥–∏ 10 –æ—Ä–∫–æ–≤ –≤ –±–æ—é");
                        break;
                    case "Mage":
                        Console.WriteLine("–ö–≤–µ—Å—Ç: –°–æ–±–µ—Ä–∏ 5 –º–∞–≥–∏—á–µ—Å–∫–∏—Ö –∫—Ä–∏—Å—Ç–∞–ª–ª–æ–≤");
                        break;
                    case "Rogue":
                        Console.WriteLine("–ö–≤–µ—Å—Ç: –£–∫—Ä–∞–¥–∏ –∞—Ä—Ç–µ—Ñ–∞–∫—Ç –∏–∑ –∑–∞–º–∫–∞");
                        break;
                    default:
                        Console.WriteLine("–ö–≤–µ—Å—Ç: –ü–æ–º–æ–≥–∏ –∂–∏—Ç–µ–ª—è–º –¥–µ—Ä–µ–≤–Ω–∏");
                        break;
                }
            }
            else
            {
                if (!questCompleted)
                {
                    Console.WriteLine("NPC: –°–Ω–∞—á–∞–ª–∞ –∑–∞–∫–æ–Ω—á–∏ –ø—Ä–µ–¥—ã–¥—É—â–∏–π –∫–≤–µ—Å—Ç!");
                }
                else
                {
                    Console.WriteLine("NPC: –ü–æ–≤—ã—Å—å —Å–≤–æ—é —Ä–µ–ø—É—Ç–∞—Ü–∏—é, —á—Ç–æ–±—ã –ø–æ–ª—É—á–∏—Ç—å –∫–≤–µ—Å—Ç.");
                }
            }

            // ========================================
            // 9. –ò–ì–†–û–í–û–ô –ü–†–ò–ú–ï–†: –ë–û–ï–í–ê–Ø –°–ò–°–¢–ï–ú–ê
            // ========================================

            Console.WriteLine("\n=== –ò–≥—Ä–æ–≤–æ–π –ø—Ä–∏–º–µ—Ä: –ë–æ–µ–≤–∞—è —Å–∏—Å—Ç–µ–º–∞ ===");

            int playerHP = 80;
            int enemyHP = 50;
            int playerAttack = 25;
            int enemyAttack = 15;
            bool playerHasPotion = true;

            Console.WriteLine($"–ò–≥—Ä–æ–∫: {playerHP} HP");
            Console.WriteLine($"–í—Ä–∞–≥: {enemyHP} HP\n");

            // –•–æ–¥ –∏–≥—Ä–æ–∫–∞
            if (playerHP <= 30 && playerHasPotion)
            {
                Console.WriteLine("üß™ –ò–≥—Ä–æ–∫ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç –∑–µ–ª—å–µ!");
                playerHP += 40;
                playerHasPotion = false;
                Console.WriteLine($"–ó–¥–æ—Ä–æ–≤—å–µ –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ: {playerHP} HP");
            }
            else
            {
                Console.WriteLine($"‚öîÔ∏è –ò–≥—Ä–æ–∫ –∞—Ç–∞–∫—É–µ—Ç! –£—Ä–æ–Ω: {playerAttack}");
                enemyHP -= playerAttack;

                if (enemyHP <= 0)
                {
                    Console.WriteLine("üéâ –í—Ä–∞–≥ –ø–æ–≤–µ—Ä–∂–µ–Ω! –ü–æ–±–µ–¥–∞!");
                }
                else
                {
                    Console.WriteLine($"–ó–¥–æ—Ä–æ–≤—å–µ –≤—Ä–∞–≥–∞: {enemyHP} HP");
                }
            }

            // –•–æ–¥ –≤—Ä–∞–≥–∞ (–µ—Å–ª–∏ –∂–∏–≤)
            if (enemyHP > 0)
            {
                Console.WriteLine($"\nüëπ –í—Ä–∞–≥ –∞—Ç–∞–∫—É–µ—Ç! –£—Ä–æ–Ω: {enemyAttack}");
                playerHP -= enemyAttack;

                if (playerHP <= 0)
                {
                    Console.WriteLine("üíÄ –ò–≥—Ä–æ–∫ –ø–æ–≥–∏–±! –ü–æ—Ä–∞–∂–µ–Ω–∏–µ...");
                }
                else
                {
                    Console.WriteLine($"–ó–¥–æ—Ä–æ–≤—å–µ –∏–≥—Ä–æ–∫–∞: {playerHP} HP");

                    // –ü—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–µ –æ –Ω–∏–∑–∫–æ–º –∑–¥–æ—Ä–æ–≤—å–µ
                    if (playerHP <= 20)
                    {
                        Console.WriteLine("‚ö†Ô∏è –ö–†–ò–¢–ò–ß–ï–°–ö–û–ï –°–û–°–¢–û–Ø–ù–ò–ï!");
                    }
                }
            }
        }
    }
