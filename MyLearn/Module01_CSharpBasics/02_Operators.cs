using System;

namespace MyLearn.Module01_CSharpBasics
{
    /// <summary>
    /// Lesson 2: Operators –≤ C#
    /// 
    /// –í —ç—Ç–æ–º —É—Ä–æ–∫–µ –≤—ã –∏–∑—É—á–∏—Ç–µ:
    /// - –ê—Ä–∏—Ñ–º–µ—Ç–∏—á–µ—Å–∫–∏–µ operators (+, -, *, /, %)
    /// - Operators —Å—Ä–∞–≤–Ω–µ–Ω–∏—è (==, !=, >, <, >=, <=)
    /// - –õ–æ–≥–∏—á–µ—Å–∫–∏–µ operators (&&, ||, !)
    /// - Operators –ø—Ä–∏—Å–≤–∞–∏–≤–∞–Ω–∏—è (=, +=, -=, *=, /=)
    /// - –ò–Ω–∫—Ä–µ–º–µ–Ω—Ç –∏ –¥–µ–∫—Ä–µ–º–µ–Ω—Ç (++, --)
    /// </summary>
    public class Operators
    {
        // –î–ª—è –∑–∞–ø—É—Å–∫–∞ —ç—Ç–æ–≥–æ —É—Ä–æ–∫–∞ —Ä–∞—Å–∫–æ–º–º–µ–Ω—Ç–∏—Ä—É–π—Ç–µ Main –∏ –∑–∞–∫–æ–º–º–µ–Ω—Ç–∏—Ä—É–π—Ç–µ Main –≤ –¥—Ä—É–≥–∏—Ö —Ñ–∞–π–ª–∞—Ö
        public static void RunDemo()
        {
            Console.WriteLine("=== Lesson 2: Operators ===\n");

            // ========================================
            // 1. –ê–†–ò–§–ú–ï–¢–ò–ß–ï–°–ö–ò–ï –û–ü–ï–†–ê–¢–û–†–´
            // ========================================

            Console.WriteLine("--- –ê—Ä–∏—Ñ–º–µ—Ç–∏—á–µ—Å–∫–∏–µ operators ---");

            int damage = 20;
            int armor = 5;

            // –°–ª–æ–∂–µ–Ω–∏–µ (+)
            int totalDamage = damage + 10;
            Console.WriteLine($"–£—Ä–æ–Ω + –±–æ–Ω—É—Å: {damage} + 10 = {totalDamage}");

            // –í—ã—á–∏—Ç–∞–Ω–∏–µ (-)
            int finalDamage = damage - armor;
            Console.WriteLine($"–£—Ä–æ–Ω –ø–æ—Å–ª–µ –±—Ä–æ–Ω–∏: {damage} - {armor} = {finalDamage}");

            // –£–º–Ω–æ–∂–µ–Ω–∏–µ (*)
            int criticalDamage = damage * 2;
            Console.WriteLine($"–ö—Ä–∏—Ç–∏—á–µ—Å–∫–∏–π —É—Ä–æ–Ω: {damage} * 2 = {criticalDamage}");

            // –î–µ–ª–µ–Ω–∏–µ (/)
            int halfDamage = damage / 2;
            Console.WriteLine($"–ü–æ–ª–æ–≤–∏–Ω–∞ —É—Ä–æ–Ω–∞: {damage} / 2 = {halfDamage}");

            // –û—Å—Ç–∞—Ç–æ–∫ –æ—Ç –¥–µ–ª–µ–Ω–∏—è (%)
            int remainder = 17 % 5;  // 17 –¥–µ–ª–∏–º –Ω–∞ 5 = 3 —Å –æ—Å—Ç–∞—Ç–∫–æ–º 2
            Console.WriteLine($"–û—Å—Ç–∞—Ç–æ–∫ –æ—Ç –¥–µ–ª–µ–Ω–∏—è: 17 % 5 = {remainder}\n");

            // ========================================
            // 2. –û–ü–ï–†–ê–¢–û–†–´ –°–†–ê–í–ù–ï–ù–ò–Ø
            // ========================================

            Console.WriteLine("--- Operators —Å—Ä–∞–≤–Ω–µ–Ω–∏—è ---");

            int playerHealth = 80;
            int maxHealth = 100;
            int enemyHealth = 50;

            // –†–∞–≤–Ω–æ (==)
            bool isFullHealth = playerHealth == maxHealth;
            Console.WriteLine($"–ü–æ–ª–Ω–æ–µ –∑–¥–æ—Ä–æ–≤—å–µ? {playerHealth} == {maxHealth} = {isFullHealth}");

            // –ù–µ —Ä–∞–≤–Ω–æ (!=)
            bool isDamaged = playerHealth != maxHealth;
            Console.WriteLine($"–ü–æ–ª—É—á–µ–Ω —É—Ä–æ–Ω? {playerHealth} != {maxHealth} = {isDamaged}");

            // –ë–æ–ª—å—à–µ (>)
            bool playerStronger = playerHealth > enemyHealth;
            Console.WriteLine($"–ò–≥—Ä–æ–∫ —Å–∏–ª—å–Ω–µ–µ? {playerHealth} > {enemyHealth} = {playerStronger}");

            // –ú–µ–Ω—å—à–µ (<)
            bool needsHealing = playerHealth < maxHealth;
            Console.WriteLine($"–ù—É–∂–Ω–æ –ª–µ—á–µ–Ω–∏–µ? {playerHealth} < {maxHealth} = {needsHealing}");

            // –ë–æ–ª—å—à–µ –∏–ª–∏ —Ä–∞–≤–Ω–æ (>=)
            bool canSurvive = playerHealth >= 50;
            Console.WriteLine($"–ú–æ–∂–µ—Ç –≤—ã–∂–∏—Ç—å? {playerHealth} >= 50 = {canSurvive}");

            // –ú–µ–Ω—å—à–µ –∏–ª–∏ —Ä–∞–≤–Ω–æ (<=)
            bool lowHealth = playerHealth <= 30;
            Console.WriteLine($"–ú–∞–ª–æ –∑–¥–æ—Ä–æ–≤—å—è? {playerHealth} <= 30 = {lowHealth}\n");

            // ========================================
            // 3. –õ–û–ì–ò–ß–ï–°–ö–ò–ï –û–ü–ï–†–ê–¢–û–†–´
            // ========================================

            Console.WriteLine("--- –õ–æ–≥–∏—á–µ—Å–∫–∏–µ operators ---");

            bool hasKey = true;
            bool doorUnlocked = false;
            int level = 5;

            // –ò (&&) - –æ–±–∞ conditions –¥–æ–ª–∂–Ω—ã –±—ã—Ç—å true
            bool canOpenDoor = hasKey && !doorUnlocked;
            Console.WriteLine($"–ú–æ–∂–µ—Ç –æ—Ç–∫—Ä—ã—Ç—å –¥–≤–µ—Ä—å? (–µ—Å—Ç—å –∫–ª—é—á –ò –¥–≤–µ—Ä—å –∑–∞–∫—Ä—ã—Ç–∞) = {canOpenDoor}");

            // –ò–õ–ò (||) - —Ö–æ—Ç—è –±—ã –æ–¥–Ω–æ —É—Å–ª–æ–≤–∏–µ true
            bool canProgress = hasKey || level >= 10;
            Console.WriteLine($"–ú–æ–∂–µ—Ç –ø—Ä–æ–π—Ç–∏? (–µ—Å—Ç—å –∫–ª—é—á –ò–õ–ò —É—Ä–æ–≤–µ–Ω—å >= 10) = {canProgress}");

            // –ù–ï (!) - –∏–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ—Ç –∑–Ω–∞—á–µ–Ω–∏–µ
            bool doorLocked = !doorUnlocked;
            Console.WriteLine($"–î–≤–µ—Ä—å –∑–∞–ø–µ—Ä—Ç–∞? (!–¥–≤–µ—Ä—å –æ—Ç–∫—Ä—ã—Ç–∞) = {doorLocked}");

            // –°–ª–æ–∂–Ω—ã–µ conditions
            bool canFight = playerHealth > 20 && enemyHealth > 0;
            Console.WriteLine($"–ú–æ–∂–µ—Ç —Å—Ä–∞–∂–∞—Ç—å—Å—è? (–∑–¥–æ—Ä–æ–≤—å–µ > 20 –ò –≤—Ä–∞–≥ –∂–∏–≤) = {canFight}\n");

            // ========================================
            // 4. –û–ü–ï–†–ê–¢–û–†–´ –ü–†–ò–°–í–ê–ò–í–ê–ù–ò–Ø
            // ========================================

            Console.WriteLine("--- Operators –ø—Ä–∏—Å–≤–∞–∏–≤–∞–Ω–∏—è ---");

            int score = 100;
            Console.WriteLine($"–ù–∞—á–∞–ª—å–Ω—ã–µ –æ—á–∫–∏: {score}");

            // –°–ª–æ–∂–µ–Ω–∏–µ —Å –ø—Ä–∏—Å–≤–∞–∏–≤–∞–Ω–∏–µ–º (+=)
            score += 50;  // –¢–æ –∂–µ —á—Ç–æ: score = score + 50
            Console.WriteLine($"–ü–æ—Å–ª–µ += 50: {score}");

            // –í—ã—á–∏—Ç–∞–Ω–∏–µ —Å –ø—Ä–∏—Å–≤–∞–∏–≤–∞–Ω–∏–µ–º (-=)
            score -= 20;  // –¢–æ –∂–µ —á—Ç–æ: score = score - 20
            Console.WriteLine($"–ü–æ—Å–ª–µ -= 20: {score}");

            // –£–º–Ω–æ–∂–µ–Ω–∏–µ —Å –ø—Ä–∏—Å–≤–∞–∏–≤–∞–Ω–∏–µ–º (*=)
            score *= 2;   // –¢–æ –∂–µ —á—Ç–æ: score = score * 2
            Console.WriteLine($"–ü–æ—Å–ª–µ *= 2: {score}");

            // –î–µ–ª–µ–Ω–∏–µ —Å –ø—Ä–∏—Å–≤–∞–∏–≤–∞–Ω–∏–µ–º (/=)
            score /= 4;   // –¢–æ –∂–µ —á—Ç–æ: score = score / 4
            Console.WriteLine($"–ü–æ—Å–ª–µ /= 4: {score}\n");

            // ========================================
            // 5. –ò–ù–ö–†–ï–ú–ï–ù–¢ –ò –î–ï–ö–†–ï–ú–ï–ù–¢
            // ========================================

            Console.WriteLine("--- –ò–Ω–∫—Ä–µ–º–µ–Ω—Ç –∏ –¥–µ–∫—Ä–µ–º–µ–Ω—Ç ---");

            int ammo = 10;
            Console.WriteLine($"–ü–∞—Ç—Ä–æ–Ω—ã: {ammo}");

            // –î–µ–∫—Ä–µ–º–µ–Ω—Ç (--) - —É–º–µ–Ω—å—à–µ–Ω–∏–µ –Ω–∞ 1
            ammo--;  // –¢–æ –∂–µ —á—Ç–æ: ammo = ammo - 1
            Console.WriteLine($"–ü–æ—Å–ª–µ –≤—ã—Å—Ç—Ä–µ–ª–∞ (ammo--): {ammo}");

            // –ò–Ω–∫—Ä–µ–º–µ–Ω—Ç (++) - —É–≤–µ–ª–∏—á–µ–Ω–∏–µ –Ω–∞ 1
            ammo++;  // –¢–æ –∂–µ —á—Ç–æ: ammo = ammo + 1
            Console.WriteLine($"–ü–æ—Å–ª–µ –ø–æ–¥–±–æ—Ä–∞ –ø–∞—Ç—Ä–æ–Ω–∞ (ammo++): {ammo}");

            // –ü—Ä–µ—Ñ–∏–∫—Å–Ω–∞—è –∏ –ø–æ—Å—Ç—Ñ–∏–∫—Å–Ω–∞—è —Ñ–æ—Ä–º–∞
            int a = 5;
            int b = ++a;  // –°–Ω–∞—á–∞–ª–∞ —É–≤–µ–ª–∏—á–∏–≤–∞–µ—Ç, –ø–æ—Ç–æ–º –ø—Ä–∏—Å–≤–∞–∏–≤–∞–µ—Ç (a=6, b=6)
            Console.WriteLine($"–ü—Ä–µ—Ñ–∏–∫—Å–Ω—ã–π –∏–Ω–∫—Ä–µ–º–µ–Ω—Ç: a={a}, b={b}");

            int c = 5;
            int d = c++;  // –°–Ω–∞—á–∞–ª–∞ –ø—Ä–∏—Å–≤–∞–∏–≤–∞–µ—Ç, –ø–æ—Ç–æ–º —É–≤–µ–ª–∏—á–∏–≤–∞–µ—Ç (c=6, d=5)
            Console.WriteLine($"–ü–æ—Å—Ç—Ñ–∏–∫—Å–Ω—ã–π –∏–Ω–∫—Ä–µ–º–µ–Ω—Ç: c={c}, d={d}\n");

            // ========================================
            // 6. –ü–†–ò–û–†–ò–¢–ï–¢ –û–ü–ï–†–ê–¢–û–†–û–í
            // ========================================

            Console.WriteLine("--- –ü—Ä–∏–æ—Ä–∏—Ç–µ—Ç –æ–ø–µ—Ä–∞—Ç–æ—Ä–æ–≤ ---");

            int result1 = 10 + 5 * 2;      // –°–Ω–∞—á–∞–ª–∞ —É–º–Ω–æ–∂–µ–Ω–∏–µ, –ø–æ—Ç–æ–º —Å–ª–æ–∂–µ–Ω–∏–µ
            Console.WriteLine($"10 + 5 * 2 = {result1}");  // 20

            int result2 = (10 + 5) * 2;    // –°–∫–æ–±–∫–∏ –∏–º–µ—é—Ç –≤—ã—Å—à–∏–π –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç
            Console.WriteLine($"(10 + 5) * 2 = {result2}");  // 30

            bool result3 = 10 > 5 && 3 < 7;  // –°–Ω–∞—á–∞–ª–∞ —Å—Ä–∞–≤–Ω–µ–Ω–∏—è, –ø–æ—Ç–æ–º &&
            Console.WriteLine($"10 > 5 && 3 < 7 = {result3}\n");  // true

            // ========================================
            // 7. –ò–ì–†–û–í–û–ô –ü–†–ò–ú–ï–†: –ö–ê–õ–¨–ö–£–õ–Ø–¢–û–† –£–†–û–ù–ê
            // ========================================

            Console.WriteLine("=== –ò–≥—Ä–æ–≤–æ–π –ø—Ä–∏–º–µ—Ä: –ö–∞–ª—å–∫—É–ª—è—Ç–æ—Ä —É—Ä–æ–Ω–∞ ===");

            // –ë–∞–∑–æ–≤—ã–µ —Ö–∞—Ä–∞–∫—Ç–µ—Ä–∏—Å—Ç–∏–∫–∏
            int baseDamage = 50;
            int strength = 20;
            int enemyArmor = 15;
            bool isCritical = true;
            bool hasBuffActive = true;

            Console.WriteLine($"–ë–∞–∑–æ–≤—ã–π —É—Ä–æ–Ω: {baseDamage}");
            Console.WriteLine($"–°–∏–ª–∞: {strength}");
            Console.WriteLine($"–ë—Ä–æ–Ω—è –≤—Ä–∞–≥–∞: {enemyArmor}");
            Console.WriteLine($"–ö—Ä–∏—Ç–∏—á–µ—Å–∫–∏–π —É–¥–∞—Ä: {isCritical}");
            Console.WriteLine($"–ê–∫—Ç–∏–≤–µ–Ω –±–∞—Ñ—Ñ: {hasBuffActive}\n");

            // –†–∞—Å—á–µ—Ç —É—Ä–æ–Ω–∞
            int totalDamageCalc = baseDamage;

            // –î–æ–±–∞–≤–ª—è–µ–º –±–æ–Ω—É—Å –æ—Ç —Å–∏–ª—ã
            totalDamageCalc += strength;
            Console.WriteLine($"–ü–æ—Å–ª–µ –¥–æ–±–∞–≤–ª–µ–Ω–∏—è —Å–∏–ª—ã: {totalDamageCalc}");

            // –ö—Ä–∏—Ç–∏—á–µ—Å–∫–∏–π —É–¥–∞—Ä —É–¥–≤–∞–∏–≤–∞–µ—Ç —É—Ä–æ–Ω
            if (isCritical)
            {
                totalDamageCalc *= 2;
                Console.WriteLine($"–ö—Ä–∏—Ç–∏—á–µ—Å–∫–∏–π —É–¥–∞—Ä! –£—Ä–æ–Ω x2: {totalDamageCalc}");
            }

            // –ë–∞—Ñ—Ñ –¥–æ–±–∞–≤–ª—è–µ—Ç 50% —É—Ä–æ–Ω–∞
            if (hasBuffActive)
            {
                totalDamageCalc += totalDamageCalc / 2;
                Console.WriteLine($"–ë–∞—Ñ—Ñ –∞–∫—Ç–∏–≤–µ–Ω! +50% —É—Ä–æ–Ω–∞: {totalDamageCalc}");
            }

            // –í—ã—á–∏—Ç–∞–µ–º –±—Ä–æ–Ω—é –≤—Ä–∞–≥–∞
            totalDamageCalc -= enemyArmor;
            Console.WriteLine($"–ü–æ—Å–ª–µ –≤—ã—á–µ—Ç–∞ –±—Ä–æ–Ω–∏: {totalDamageCalc}");

            // –ú–∏–Ω–∏–º–∞–ª—å–Ω—ã–π —É—Ä–æ–Ω = 1
            if (totalDamageCalc < 1)
            {
                totalDamageCalc = 1;
            }

            Console.WriteLine($"\nüí• –ò—Ç–æ–≥–æ–≤—ã–π —É—Ä–æ–Ω: {totalDamageCalc}");

            // ========================================
            // 8. –ò–ì–†–û–í–û–ô –ü–†–ò–ú–ï–†: –°–ò–°–¢–ï–ú–ê –û–ü–´–¢–ê
            // ========================================

            Console.WriteLine("\n=== –ò–≥—Ä–æ–≤–æ–π –ø—Ä–∏–º–µ—Ä: –°–∏—Å—Ç–µ–º–∞ –æ–ø—ã—Ç–∞ ===");

            int currentExp = 450;
            int expToNextLevel = 500;
            int expGained = 75;

            Console.WriteLine($"–¢–µ–∫—É—â–∏–π –æ–ø—ã—Ç: {currentExp}/{expToNextLevel}");
            Console.WriteLine($"–ü–æ–ª—É—á–µ–Ω–æ –æ–ø—ã—Ç–∞: +{expGained}");

            currentExp += expGained;
            Console.WriteLine($"–ù–æ–≤—ã–π –æ–ø—ã—Ç: {currentExp}/{expToNextLevel}");

            bool leveledUp = currentExp >= expToNextLevel;
            Console.WriteLine($"–ü–æ–≤—ã—à–µ–Ω–∏–µ —É—Ä–æ–≤–Ω—è: {leveledUp}");

            if (leveledUp)
            {
                currentExp -= expToNextLevel;
                Console.WriteLine($"üéâ Level –ø–æ–≤—ã—à–µ–Ω! –û—Å—Ç–∞—Ç–æ–∫ –æ–ø—ã—Ç–∞: {currentExp}");
            }
        }
    }
}

